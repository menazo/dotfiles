end

local lsp_installer = require("nvim-lsp-installer")
local util = require('lspconfig.util')

-- Register a handler that will be called for each installed server when it's ready (i.e. when installation is finished
-- or if the server is already installed).
lsp_installer.on_server_ready(function(server)
  local opts = { on_attach = on_attach, capabilities = capabilities }
  if server.name == "sumneko_lua" then
    print("lua server setup")
    opts.settings = {
      Lua = {
        diagnostics = {
          globals = { 'vim', 'use' }
        }
      }
    }
  end
  if server.name == "pylsp" then
    print('pylsp setup')
    opts.settings = {
      pylsp = {
        configurationSources = {"flake8", "pylint"},
        root_dir = function(fname)
          local root_files = {
            'pyproject.toml',
            'setup.py',
            'setup.cfg',
          }
          return util.root_pattern(unpack(root_files))(fname) or util.find_git_ancestor(fname)
        end,
        singe_file_support =  true,
        plugins = {
          flake8 = {
            enabled = true
          },
          pydocstyle = {
            enabled = true,
            maxLineLength = 76,
            convention = 'google'
          },
          pyflakes = { enabled = true },
          pylint = { enabled = true },
          jedi_completion = {
            enabled = true,
            include_params = true,  -- this line enables snippets
            cache_for = {"pandas", "numpy", "tensorflow", "matplotlib", "pandera"}
          }
        }
      }
    }
  end
  -- if server.name == "terraformls" then
  -- print("tf setup")
  -- opts.filetypes = { 'terraform', 'tf', 'tftmpl' }
  -- end
  -- (optional) Customize the options passed to the server
  -- if server.name == "tsserver" then
  --     opts.root_dir = function() ... end
  -- end

  -- This setup() function will take the provided server configuration and decorate it with the necessary properties
  -- before passing it onwards to lspconfig.
  -- Refer to https://github.com/neovim/nvim-lspconfig/blob/master/doc/server_configurations.md
  server:setup(opts)
end)

